using DatingApp.API.Data;
using Microsoft.AspNetCore.Mvc;
using Microsoft.EntityFrameworkCore;
using Microsoft.Extensions.Logging;

namespace DatingApp.API.Controllers
{
    [ApiController]
    [Route("[controller]")]
    public class WeatherForecastController : ControllerBase
    {        
        private static readonly string[] Summaries = new[]
        {
            "Freezing", "Bracing", "Chilly", "Cool", "Mild", "Warm", "Balmy", "Hot", "Sweltering", "Scorching"
        };

        private readonly ILogger<WeatherForecastController> _logger;
        private readonly DataContext _context;


        public WeatherForecastController(ILogger<WeatherForecastController> logger, DataContext context)
        {
            _logger = logger;
            _context = context;
        }

        [HttpGet]
        public async  Task<IActionResult> GetWeatherForecsts()
        {
            var weatherForecasts = await _context.WeatherForecasts.ToListAsync();

            return Ok(weatherForecasts);
        }

        [HttpGet("{Id}")]
        public async Task<IActionResult> GetWeatherForecst(int id)
        {
            var weatherForecast = await _context.WeatherForecasts.FirstOrDefaultAsync(x => x.Id == id);

            return Ok(weatherForecast);
        }
